// Convert cm to m and km

#include<stdio.h>
int main()
{float centimeters,meters,kilometers;
printf("Enter the length in centimeters : ");
scanf("%f",&centimeters);
meters=centimeters/100;
kilometers=centimeters/100000;
printf("Length in meters is : %f m",meters);
printf("\nLength in kilometers is : %f km",kilometers);
return 0;
}
